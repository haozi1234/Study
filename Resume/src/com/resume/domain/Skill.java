package com.resume.domain;

public class Skill {

	private String id;
	private String uid;
	private String proficient;
	private String conversant;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getProficient() {
		return proficient;
	}
	public void setProficient(String proficient) {
		this.proficient = proficient;
	}
	public String getConversant() {
		return conversant;
	}
	public void setConversant(String conversant) {
		this.conversant = conversant;
	}
}
